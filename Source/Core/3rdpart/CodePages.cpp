int CodepageByName(const std::string& name)
{
    auto it = langs.find(name);
    if (it != langs.end())
    {
        return it->second;
    }
    return 65001;
}

std::string NameByCodepage( int code )
{
    auto it = std::find_if(langs.begin(), langs.end(), [code](decltype(*langs.begin())& v) { return v.second == code; });
    if (it != langs.end())
    {
        return it->first;
    }
    return std::string();
}